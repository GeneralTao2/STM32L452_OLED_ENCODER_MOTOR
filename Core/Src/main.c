void I2C_Scan() {
    char LCD_Buffer[] = "Scanning I2C bus...\r\n";
    HAL_UART_Transmit(&huart2, (uint8_t*)LCD_Buffer, strlen(LCD_Buffer), HAL_MAX_DELAY);

    HAL_StatusTypeDef res;
    for(uint16_t i = 0; i < 128; i++) {
        res = HAL_I2C_IsDeviceReady(&hi2c1, i << 1, 1, 10);
        if(res == HAL_OK) {
            char msg[64];
            snprintf(msg, sizeof(msg), "0x%02X", i);
            HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
        } else {
            HAL_UART_Transmit(&huart2, (uint8_t*)".", 1, HAL_MAX_DELAY);
        }
    }
    HAL_UART_Transmit(&huart2, (uint8_t*)"\r\n", 2, HAL_MAX_DELAY);
}

void LCD_PrintEncoderState() {
	if(HAL_GetTick() - LCD_progTime > LCD_PROGTIME_DELAY) {
		ssd1306_Fill(Black);

		ssd1306_SetCursor(10, 5);
		ssd1306_WriteString("MOTOR", Font_11x18, White);

		ssd1306_SetCursor(10, 23);
		sprintf(LCD_Buffer, "Speed: %d", encoder.tumblerState);
		ssd1306_WriteString(LCD_Buffer, Font_11x18, White);

		ssd1306_SetCursor(10, 40);
		sprintf(LCD_Buffer, "State: ");
		strcat(LCD_Buffer, encoder.buttonState ? "ON" : "OFF");
		ssd1306_WriteString(LCD_Buffer, Font_11x18, White);

		ssd1306_UpdateScreen();

		LCD_progTime = HAL_GetTick();
	}
}

